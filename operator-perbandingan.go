package main

func main() {

	var x int = 5
	var y int = 3

	println("Sama dengan : ", x == y)
	println("Tidak sama dengan : ", x == y)
	println("Lebih besar dari : ", x > y)
	println("Lebih kecil dari : ", x < y)
	println("Lebih besar dari sama dengan : ", x >= y)
	println("Lebih kecil dari sama dengan : ", x <= y)
}